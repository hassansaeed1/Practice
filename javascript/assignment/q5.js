
var method=prompt("Enter operator(either +,-,*,/ & %)")
var firstname=parseInt(prompt("enter first number: "))
var secondname=parseInt(prompt("enter second number: "))
if(method=="+"){
    alert(firstname+secondname)
}
else if(method=="-"){
    alert(firstname+secondname)
}
else if(method=="*"){
    alert(firstname-secondname)
}
else if(method=="*"){
    alert(firstname*secondname)
}
else if(method=="/"){
    alert(firstname/secondname)
}
else if(method=="%"){
    alert(firstname/secondname)*100
}
