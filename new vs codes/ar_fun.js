"use strict";
//Arrow Functions
let sum1 = (a, b) => {
    return a * b;
};
sum1(26, 4);
console.log(sum1(26, 4));
let sum2 = (a, b) => a ** b;
sum2(3, 4);
console.log(sum2(3, 4));
let sum3 = (a, b) => a / b;
sum3(7, 2);
let sum4 = (a, b) => a + b;
sum4(2, 4);
console.log(sum1(26, 4), sum2(3, 4), sum3(7, 2), sum4(2, 4));
